package other;

public class ExecResult
{
    private String msg;
    private Boolean success;
    
    public String getMsg() {
        return this.msg;
    }
    
    public void setMsg(final String msg) {
        this.msg = msg;
    }
    
    public Boolean getSuccess() {
        return this.success;
    }
    
    public void setSuccess(final Boolean success) {
        this.success = success;
    }
}
